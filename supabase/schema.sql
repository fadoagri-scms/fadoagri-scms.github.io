-- ============ Xưởng Ba Phi: Tồn kho thành phẩm theo lô ============
-- 1 dòng/lô hàng — theo dõi đã xuất bao nhiêu, còn lại bao nhiêu so với tổng
-- thành phẩm (tổng hợp từ Sản xuất = sum(factory_batches.finished_qty) theo
-- từng lô hàng trong raw_batches).
create table if not exists public.factory_finished_stock (
  id bigint generated by default as identity primary key,
  batch text not null,        -- lô hàng (khớp raw_batches.batch)
  export_date date,           -- ngày xuất hàng
  exported_qty numeric,       -- số lượng đã xuất (trái)
  created_at timestamptz default now()
);
alter table public.factory_finished_stock enable row level security;
drop policy if exists "Public full access" on public.factory_finished_stock;
create policy "Public full access" on public.factory_finished_stock for all using (true) with check (true);
do $$
begin
  if not exists (
    select 1 from information_schema.table_constraints
    where table_schema = 'public' and table_name = 'factory_finished_stock'
      and constraint_name = 'factory_finished_stock_batch_key'
  ) then
    alter table public.factory_finished_stock add constraint factory_finished_stock_batch_key unique (batch);
  end if;
end $$;

