-- ============ Dữ liệu mẫu (giữ nguyên nội dung minh họa hiện tại) ============
-- Lưu ý: module Nhà cung cấp / PO chỉ dùng cho hàng thương mại (Chanh, Thanh
-- long, Chuối...). Dừa đi theo flow riêng (Vùng nguyên liệu → Xưởng dừa Ba
-- Phi → QC) nên không tạo dữ liệu mẫu Dừa ở đây.
--
-- Các bảng không có ràng buộc unique trên khóa nghiệp vụ (batch_code, po_code...)
-- vì người dùng có thể hợp lệ tạo nhiều dòng cùng khóa đó qua form (VD: 1 lô
-- có 2 lần kiểm QC). Vì vậy phần seed dưới đây dùng "insert ... where not
-- exists" (thay vì "on conflict do nothing", vốn không có tác dụng nếu không
-- có unique constraint) để chạy lại file này nhiều lần không bị nhân đôi dữ
-- liệu mẫu.

