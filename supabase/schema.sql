-- ============ Xưởng Ba Phi: Sản xuất ============
-- Mỗi dòng = 1 lượt chế biến cho ĐÚNG 1 đợt nhập nguyên liệu (raw_batches),
-- không còn theo lô hàng gộp nữa — vì 1 lô hàng có thể có nhiều đợt nhập từ
-- nhiều NCC, mỗi đợt xử lý/hao hụt riêng. batch_code/stage/progress_pct/
-- input_kg/output_kg/order_code/customer_name là cột cũ, không còn dùng
-- trong giao diện nhưng giữ lại để không mất dữ liệu lịch sử.
create table if not exists public.factory_batches (
  id bigint generated by default as identity primary key,
  batch_code text not null,
  stage text,               -- công đoạn hiện tại (cũ, không còn dùng)
  progress_pct integer default 0 check (progress_pct between 0 and 100),
  start_time text,
  expected_finish text,
  input_kg numeric,
  output_kg numeric,
  duration_hours numeric,
  created_at timestamptz default now()
);
alter table public.factory_batches add column if not exists order_code text;   -- mã đơn hàng (cũ, không còn dùng)
alter table public.factory_batches add column if not exists customer_name text; -- khách hàng (cũ, không còn dùng)
alter table public.factory_batches drop constraint if exists factory_batches_batch_code_key;
alter table public.factory_batches add column if not exists raw_batch_id bigint references public.raw_batches(id);
alter table public.factory_batches add column if not exists production_date date;   -- ngày sản xuất
alter table public.factory_batches add column if not exists finished_qty numeric;   -- thành phẩm (trái)
alter table public.factory_batches enable row level security;
drop policy if exists "Public full access" on public.factory_batches;
create policy "Public full access" on public.factory_batches for all using (true) with check (true);
-- Mỗi đợt nhập nguyên liệu chỉ có đúng 1 dòng theo dõi sản xuất.
do $$
begin
  if not exists (
    select 1 from information_schema.table_constraints
    where table_schema = 'public' and table_name = 'factory_batches'
      and constraint_name = 'factory_batches_raw_batch_id_key'
  ) then
    alter table public.factory_batches add constraint factory_batches_raw_batch_id_key unique (raw_batch_id);
  end if;
end $$;

